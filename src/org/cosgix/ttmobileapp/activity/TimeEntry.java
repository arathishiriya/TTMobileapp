package org.cosgix.ttmobileapp.activity;

/**
 * This class provides the Time entry getter and setter
 * @author Sanjib
 *
 */
public class TimeEntry {
	
	// variables declaration
	private String timeEntryName;
	private int timeEntryImage;
	
	// getter method for time entry name
	public String getTimeEntryName() {
		return timeEntryName;
	}
	
	// setter method for time entry name
	public void setTimeEntryName(String timeEntryName) {
		this.timeEntryName = timeEntryName;
	}
	
	// getter method for time entry image
	public int getTimeEntryImage() {
		return timeEntryImage;
	}
	
	// setter method for time entry image
	public void setTimeEntryImage(int timeEntryImage) {
		this.timeEntryImage = timeEntryImage;
	}

}
